import { test, expect } from 'bun:test'
import { UserData } from '../../src/builders/user-data'

test('UserData builds UnifiedUserData with all fields', () => {
  const user = new UserData()
    .setEmail('joao@email.com')
    .setPhone('+5511999999999')
    .setFirstName('Joao')
    .setLastName('Silva')
    .setClientIpAddress('189.0.0.1')
    .setClientUserAgent('Mozilla/5.0')
    .setExternalId('12345')
    .setCity('Sao Paulo')
    .setState('SP')
    .setZipCode('01001-000')
    .setCountry('BR')
    .setBirthDate('19900115')
    .setGender('m')
    .setGaClientId('GA1.2.123456789.1234567890')
    .setFbp('fb.1.1234567890.123456789')
    .setFbc('fb.1.1234567890.AbCdEfGhIj')
    .setGclid('CjwKCAjw...')
    .setDeviceId('device-abc')
    .toUnifiedUserData()

  expect(user.email).toBe('joao@email.com')
  expect(user.phone).toBe('+5511999999999')
  expect(user.firstName).toBe('Joao')
  expect(user.lastName).toBe('Silva')
  expect(user.ip).toBe('189.0.0.1')
  expect(user.userAgent).toBe('Mozilla/5.0')
  expect(user.externalId).toBe('12345')
  expect(user.city).toBe('Sao Paulo')
  expect(user.state).toBe('SP')
  expect(user.zipCode).toBe('01001-000')
  expect(user.country).toBe('BR')
  expect(user.birthDate).toBe('19900115')
  expect(user.gender).toBe('m')
  expect(user.gaClientId).toBe('GA1.2.123456789.1234567890')
  expect(user.fbp).toBe('fb.1.1234567890.123456789')
  expect(user.fbc).toBe('fb.1.1234567890.AbCdEfGhIj')
  expect(user.gclid).toBe('CjwKCAjw...')
  expect(user.deviceId).toBe('device-abc')
})

test('UserData returns a copy (not a reference)', () => {
  const builder = new UserData().setEmail('a@b.com')
  const data1 = builder.toUnifiedUserData()
  const data2 = builder.toUnifiedUserData()
  expect(data1).toEqual(data2)
  expect(data1).not.toBe(data2)
})
